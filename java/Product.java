class Product {
	// atribut private
    private String price;
	private String idProduct;

	// setter/getter
	public void setprice(String price) {
		this.price = price;
	}
	public String getprice() {
		return price;
	}
	public void setidProduct(String idProduct) {
		this.idProduct = idProduct;
	}
	public String getidProduct() {
		return idProduct;
	}
}